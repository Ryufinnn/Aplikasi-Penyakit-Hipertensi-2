<p> &nbsp;&nbsp;&nbsp;&nbsp;Hipertensi atau  tekananan darah tinggi merupakan gangguan pada sistem peredaran darah yang  dapat menyebabkan kenaikan tekanan darah di atas nilai normal, yaitu melebihi  140 / 90 mmHg. Hipertensi dalam bahasa inggrisnya adalah Hypertension,  Hypertension berasal dari dua kata yaitu Hyper yang berarti tinggi, dan Tension  yang berarti tegangan.<br />
Ketika dilakukan pemeriksaan tekanan darah menghasilkan dua angka, yaitu angka  yang lebih tinggi dan angka yang lebih rendah. Angka yang lebih tinggi didapat  ketika jantung berkontraksi (sistolik), sedangkan angka yang lebih rendah  didapatkan ketika jantuk berelaksasi (diastolik). Tekanan darah kurang dari 120  / 80 mmHg dapat diartikan sebagai tekanan darah yang normal. Ketika terjadi tekanan darah tinggi, umumnya terjadi kenaikan  tekanan sistolik dan diastolik. Hipertensi umumnya terjadi ketika tekanan darah mencapai 140 / 90 mmHG atau lebih,  pengukuran tekanan darah ini dilakukan pada lengan tiga kali dalam jangka  beberapa minggu. </p>
